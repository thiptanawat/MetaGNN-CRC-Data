# STEP 4: Download HMA tissue-specific GEMs (for activity pseudo-labels)
# ═══════════════════════════════════════════════════════════════════════════════

def download_hma_tissue_gems(output_dir: str) -> str:
    """
    Download the 98 tissue-specific GEMs from Robinson et al. 2020
    "An Atlas of Human Metabolism" (Science Signaling).

    Source: Zenodo record 3583004
      https://zenodo.org/record/3583004

    The tINIT_GEMs/ directory in this archive contains the pre-generated
    tissue-specific models used for activity pseudo-labels.

    Returns path to the extracted data directory.
    """
    import requests
    import zipfile
    from tqdm import tqdm

    logger.info("\n" + "=" * 60)
    logger.info("STEP 4: Downloading HMA tissue-specific GEMs")
    logger.info("         (Robinson et al. 2020, Zenodo #3583004)")
    logger.info("=" * 60)

    hma_dir = os.path.join(output_dir, 'hma_tissue_gems')
    zip_path = os.path.join(output_dir, 'zenodo_3583004.zip')

    # Check if already extracted
    if os.path.isdir(hma_dir):
        mat_files = [f for f in os.listdir(hma_dir) if f.endswith('.mat')]
        if mat_files:
            logger.info(f"HMA data already exists ({len(mat_files)} .mat files) — skipping")
            # Return the first .mat file found
            return os.path.join(hma_dir, mat_files[0])

    os.makedirs(hma_dir, exist_ok=True)

    # ─── Method 1: Zenodo API ─────────────────────────────────────────────
    logger.info("Querying Zenodo API for record 3583004...")
    try:
        api_url = "https://zenodo.org/api/records/3583004"
        resp = requests.get(api_url, timeout=30)
        resp.raise_for_status()
        record = resp.json()

        # Find the main archive file
        files = record.get('files', [])
        logger.info(f"Found {len(files)} files in Zenodo record:")

        target_file = None
        for f in files:
            fname = f.get('key', f.get('filename', ''))
            fsize = f.get('size', 0)
            logger.info(f"  {fname} ({fsize / (1024**2):.1f} MB)")

            # Look for the main zip/tar archive or tINIT_GEMs specifically
            if 'tinit' in fname.lower() or fname.endswith('.zip'):
                target_file = f
            elif target_file is None:
                target_file = f  # fallback: take the first/largest file

        if target_file:
            fname = target_file.get('key', target_file.get('filename', ''))
            # Zenodo download URL
            download_url = target_file.get('links', {}).get('self', '')
            if not download_url:
                download_url = f"https://zenodo.org/records/3583004/files/{fname}?download=1"

            logger.info(f"\nDownloading: {fname}")
            logger.info(f"URL: {download_url}")

            resp = requests.get(download_url, stream=True, timeout=30)
            resp.raise_for_status()
            total = int(resp.headers.get('content-length', 0))

            dl_path = os.path.join(output_dir, fname)
            with open(dl_path, 'wb') as f:
                with tqdm(total=total, unit='B', unit_scale=True,
                          desc=fname) as pbar:
                    for chunk in resp.iter_content(chunk_size=65536):
                        f.write(chunk)
                        pbar.update(len(chunk))

            # Extract if it's a zip
            if dl_path.endswith('.zip'):
                logger.info(f"Extracting {fname}...")
                with zipfile.ZipFile(dl_path, 'r') as z:
                    # Extract .mat files to hma_dir
                    for member in z.namelist():
                        if member.endswith('.mat'):
                            # Extract with flat name
                            basename = os.path.basename(member)
                            target = os.path.join(hma_dir, basename)
                            with z.open(member) as src, open(target, 'wb') as dst:
                                shutil.copyfileobj(src, dst)
                            logger.info(f"  Extracted: {basename}")
                    # Also extract any directory named tINIT_GEMs
                    for member in z.namelist():
                        if 'tINIT' in member or 'tinit' in member.lower():
                            z.extract(member, hma_dir)
                os.remove(dl_path)

            elif dl_path.endswith('.mat'):
                shutil.move(dl_path, os.path.join(hma_dir, fname))

            # Find the main .mat file
            mat_files = []
            for root, dirs, files_list in os.walk(hma_dir):
                for f in files_list:
                    if f.endswith('.mat'):
                        mat_files.append(os.path.join(root, f))

            if mat_files:
                logger.info(f"HMA tissue GEMs ready: {len(mat_files)} .mat files in {hma_dir}")
                return mat_files[0]  # Return first .mat file

    except Exception as e:
        logger.warning(f"Zenodo API download failed: {e}")

    # ─── Method 2: Direct Zenodo download URL ─────────────────────────────
    direct_urls = [
        "https://zenodo.org/records/3583004/files/Human-GEM-atlas-data.zip?download=1",
        "https://zenodo.org/record/3583004/files/Atlas_of_Human_Metabolism_supplementary.zip?download=1",
    ]

    for url in direct_urls:
        logger.info(f"Trying direct URL: {url}")
        try:
            resp = requests.get(url, stream=True, timeout=30, allow_redirects=True)
            if resp.status_code == 200:
                total = int(resp.headers.get('content-length', 0))
                with open(zip_path, 'wb') as f:
                    with tqdm(total=total, unit='B', unit_scale=True,
                              desc="HMA data") as pbar:
                        for chunk in resp.iter_content(chunk_size=65536):
                            f.write(chunk)
                            pbar.update(len(chunk))

                if os.path.getsize(zip_path) > 1000:
                    logger.info("Extracting...")
                    with zipfile.ZipFile(zip_path, 'r') as z:
                        z.extractall(hma_dir)
                    os.remove(zip_path)

                    mat_files = []
                    for root, dirs, files_list in os.walk(hma_dir):
                        for f in files_list:
                            if f.endswith('.mat'):
                                mat_files.append(os.path.join(root, f))
                    if mat_files:
                        logger.info(f"HMA ready: {len(mat_files)} .mat files")
                        return mat_files[0]
        except Exception as e:
            logger.warning(f"  Failed: {e}")

    # ─── Fallback: manual instructions ────────────────────────────────────
    logger.error(
        "\n╔══════════════════════════════════════════════════════════╗\n"
        "║  Could not auto-download HMA tissue GEMs.              ║\n"
        "║  Please download manually:                              ║\n"
        "║                                                         ║\n"
        "║  1. Go to: https://zenodo.org/record/3583004            ║\n"
        "║  2. Download the archive file (.zip)                    ║\n"
        "║  3. Extract the .mat file(s) from tINIT_GEMs/           ║\n"
        f"║  4. Place into: {hma_dir:<40}║\n"
        "║  5. Re-run this script                                  ║\n"
        "╚══════════════════════════════════════════════════════════╝"
    )
    return os.path.join(hma_dir, 'tINIT_GEMs.mat')


