# STEP 5: Download Recon3D XML for FBA (optional, separate from .mat)
# ═══════════════════════════════════════════════════════════════════════════════

def download_recon3d_xml(output_dir: str) -> str:
    """
    Download Recon3D SBML/XML from BiGG for FBA experiments.
    This is separate from the .mat file — used only by run_fba_expansion.py.
    """
    import requests
    from tqdm import tqdm
    import gzip

    logger.info("\n" + "=" * 60)
    logger.info("STEP 5: Downloading Recon3D.xml for FBA (optional)")
    logger.info("=" * 60)

    xml_path = os.path.join(output_dir, 'Recon3D.xml')

    if os.path.exists(xml_path):
        size_mb = os.path.getsize(xml_path) / (1024**2)
        if size_mb > 20:  # Recon3D.xml is ~28 MB uncompressed
            logger.info(f"Recon3D.xml already exists ({size_mb:.0f} MB) — skipping")
            return xml_path
        else:
            # File exists but too small — probably truncated or error page
            logger.warning(
                f"Recon3D.xml exists but only {size_mb:.1f} MB "
                f"(expected ~28 MB). Re-downloading..."
            )
            os.remove(xml_path)

    gz_path = xml_path + '.gz'
    url = "http://bigg.ucsd.edu/static/models/Recon3D.xml.gz"

    logger.info(f"Downloading: {url}")
    try:
        resp = requests.get(url, stream=True, timeout=60, allow_redirects=True)
        resp.raise_for_status()

        # Check if response is gzipped content served directly
        content_encoding = resp.headers.get('Content-Encoding', '')
        content_type = resp.headers.get('Content-Type', '')
        total = int(resp.headers.get('content-length', 0))

        logger.info(f"  Content-Type: {content_type}")
        logger.info(f"  Content-Encoding: {content_encoding}")
        logger.info(f"  Content-Length: {total / (1024**2):.1f} MB")

        # Save the raw response first
        with open(gz_path, 'wb') as f:
            with tqdm(total=total, unit='B', unit_scale=True,
                      desc="Recon3D.xml.gz") as pbar:
                for chunk in resp.iter_content(chunk_size=65536):
                    f.write(chunk)
                    pbar.update(len(chunk))

        gz_size = os.path.getsize(gz_path) / (1024**2)
        logger.info(f"  Downloaded: {gz_size:.1f} MB")

        # Try to decompress
        logger.info("Decompressing Recon3D.xml.gz...")
        try:
            with gzip.open(gz_path, 'rb') as f_in:
                with open(xml_path, 'wb') as f_out:
                    shutil.copyfileobj(f_in, f_out)

            xml_size = os.path.getsize(xml_path) / (1024**2)
            if xml_size > gz_size:
                # Successfully decompressed
                os.remove(gz_path)
                logger.info(f"Recon3D.xml ready: {xml_size:.0f} MB → {xml_path}")
            else:
                # Decompression didn't increase size — file wasn't actually gzipped
                # The server may have already decompressed it (Content-Encoding: gzip)
                os.remove(xml_path)
                os.rename(gz_path, xml_path)
                logger.info(f"Recon3D.xml ready (server pre-decompressed): {gz_size:.0f} MB")
        except gzip.BadGzipFile:
            # Not actually gzipped — the server already decompressed
            os.rename(gz_path, xml_path)
            logger.info(f"Recon3D.xml ready (not gzipped): {gz_size:.0f} MB")

        final_size = os.path.getsize(xml_path) / (1024**2)
        if final_size < 10:
            logger.warning(
                f"Recon3D.xml is only {final_size:.1f} MB — expected ~28 MB.\n"
                "The file may be truncated or the server returned an error page.\n"
                "Try downloading manually:\n"
                "  1. Open in browser: http://bigg.ucsd.edu/static/models/Recon3D.xml.gz\n"
                "  2. Decompress the .gz file\n"
                f"  3. Save to: {xml_path}"
            )

    except Exception as e:
        logger.error(f"Failed to download Recon3D.xml: {e}")
        logger.info(
            "Manual download:\n"
            "  1. Open: http://bigg.ucsd.edu/static/models/Recon3D.xml.gz\n"
            f"  2. Decompress and save to: {xml_path}\n"
        )
    return xml_path


