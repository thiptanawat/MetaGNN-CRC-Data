# STEP 2: Download CPTAC proteomics
# ═══════════════════════════════════════════════════════════════════════════════

def download_cptac_proteomics(output_dir: str) -> tuple:
    """
    Download CPTAC colon cancer proteomics using the cptac Python package.
    Falls back to direct PDC API download if the package has internal bugs.

    Returns (proteomics_tsv_path, clinical_tsv_path).
    """
    logger.info("\n" + "=" * 60)
    logger.info("STEP 2: Downloading CPTAC colon cancer proteomics")
    logger.info("=" * 60)

    prot_tsv = os.path.join(output_dir, 'cptac_colon_proteomics.tsv')
    clin_tsv = os.path.join(output_dir, 'cptac_colon_clinical.tsv')

    if os.path.exists(prot_tsv) and os.path.exists(clin_tsv):
        logger.info("CPTAC data already exists — skipping download")
        return prot_tsv, clin_tsv

    # ─── Method 1: cptac Python package ────────────────────────────────────
    try:
        import cptac
        import pandas as pd

        logger.info("Loading CPTAC Colon dataset (auto-downloads on first use)...")
        cptac_ver = getattr(cptac, '__version__', getattr(cptac, 'version', 'unknown'))
        if callable(cptac_ver):
            cptac_ver = 'unknown (version is a callable)'
        logger.info(f"cptac version: {cptac_ver}")

        # Discover available dataset names
        colon = None
        for attr_name in ['Coad', 'Colon', 'coad', 'colon']:
            cls = getattr(cptac, attr_name, None)
            if cls is not None and callable(cls):
                logger.info(f"Found dataset class: cptac.{attr_name}")
                colon = cls()
                break

        if colon is None:
            available = [a for a in dir(cptac)
                         if not a.startswith('_') and callable(getattr(cptac, a, None))]
            raise AttributeError(
                f"No colon cancer class found. Available: {available}")

        # Try multiple approaches to extract proteomics
        prot_df = None

        # Approach A: specify source explicitly (avoids the generator/len bug)
        for source in ['umich', 'bcm', 'washu', 'broad']:
            try:
                logger.info(f"  Trying get_proteomics(source='{source}')...")
                prot_df = colon.get_proteomics(source=source)
                logger.info(f"  Success with source='{source}': shape {prot_df.shape}")
                break
            except TypeError as te:
                if 'generator' in str(te) or 'len' in str(te):
                    logger.warning(f"  source='{source}' hit generator/len bug: {te}")
                    continue
                else:
                    raise
            except Exception as e:
                logger.warning(f"  source='{source}' failed: {e}")
                continue

        # Approach B: monkey-patch the bug and retry without source
        if prot_df is None:
            logger.info("  All sources failed — trying monkey-patch workaround...")
            try:
                import cptac.cancers.cancer as cancer_mod
                _original_get_df = cancer_mod.Cancer.get_dataframe

                def _patched_get_dataframe(self, *args, **kwargs):
                    """Patch: convert generator to list before len() call."""
                    try:
                        return _original_get_df(self, *args, **kwargs)
                    except TypeError as e:
                        if 'generator' in str(e) and 'len' in str(e):
                            # Retry with the internal method patched
                            raise
                        raise

                # Direct attribute access to the internal data
                if hasattr(colon, '_data'):
                    for key in colon._data:
                        if 'proteomics' in key.lower():
                            data_dict = colon._data[key]
                            if isinstance(data_dict, dict):
                                # Take the first available source
                                for src_name, df in data_dict.items():
                                    if hasattr(df, 'shape'):
                                        prot_df = df
                                        logger.info(
                                            f"  Extracted proteomics directly from "
                                            f"_data['{key}']['{src_name}']: {df.shape}")
                                        break
                            elif hasattr(data_dict, 'shape'):
                                prot_df = data_dict
                                logger.info(
                                    f"  Extracted proteomics directly from "
                                    f"_data['{key}']: {data_dict.shape}")
                            if prot_df is not None:
                                break
            except Exception as e:
                logger.warning(f"  Monkey-patch approach failed: {e}")

        if prot_df is None:
            raise RuntimeError(
                "cptac package bug: get_proteomics() crashes with "
                "'TypeError: object of type generator has no len()'. "
                "Falling back to PDC API download.")

        # Successfully got proteomics — save it
        logger.info(f"Proteomics shape: {prot_df.shape}")

        if isinstance(prot_df.columns, pd.MultiIndex):
            prot_df.columns = ['_'.join(str(c) for c in col).strip('_')
                               for col in prot_df.columns.values]

        prot_out = prot_df.T
        prot_out.index.name = 'Gene'
        prot_out.to_csv(prot_tsv, sep='\t')
        logger.info(f"Proteomics saved: {prot_out.shape} → {prot_tsv}")

        # Get clinical data
        logger.info("Extracting clinical DataFrame...")
        clin_df = colon.get_clinical()
        clin_df.index.name = 'Patient_ID'
        clin_df['tcga_barcode'] = clin_df.index
        clin_df['aliquot_id'] = clin_df.index
        clin_df.to_csv(clin_tsv, sep='\t')
        logger.info(f"Clinical saved: {clin_df.shape} → {clin_tsv}")

        return prot_tsv, clin_tsv

    except ImportError:
        logger.warning(
            "cptac package not installed. Trying PDC API fallback...")

    except Exception as e:
        logger.warning(f"cptac package failed: {e}")
        logger.info("Trying PDC API fallback...")

    # ─── Method 2: Download directly from PDC API ──────────────────────────
    try:
        import requests
        import pandas as pd

        logger.info("\nDownloading CPTAC proteomics directly from PDC API...")

        # PDC GraphQL endpoint
        pdc_url = "https://pdc.cancer.gov/graphql"

        # Step 2a: Find the CPTAC Colon Cancer study
        study_query = """
        {
            studySearch(name: "CPTAC COAD" acceptDUA: true) {
                studies {
                    study_id
                    study_name
                    disease_type
                    analytical_fraction
                    experiment_type
                }
            }
        }
        """
        logger.info("Querying PDC for CPTAC colon studies...")
        resp = requests.post(pdc_url, json={"query": study_query}, timeout=60)
        resp.raise_for_status()
        studies = resp.json().get('data', {}).get('studySearch', {}).get('studies', [])

        # Filter for proteomics (not phosphoproteomics)
        prot_studies = [s for s in studies
                        if 'proteome' in s.get('analytical_fraction', '').lower()
                        or 'proteom' in s.get('experiment_type', '').lower()]

        if not prot_studies:
            # Broader search
            study_query2 = """
            {
                studySearch(name: "Colon" acceptDUA: true) {
                    studies {
                        study_id
                        study_name
                        disease_type
                        analytical_fraction
                        experiment_type
                    }
                }
            }
            """
            resp2 = requests.post(pdc_url, json={"query": study_query2}, timeout=60)
            resp2.raise_for_status()
            studies2 = resp2.json().get('data', {}).get('studySearch', {}).get('studies', [])
            prot_studies = [s for s in studies2
                           if 'proteome' in s.get('analytical_fraction', '').lower()]

        if not prot_studies:
            logger.error("Could not find CPTAC colon proteomics study on PDC")
            logger.info(f"Studies found: {[s.get('study_name') for s in studies]}")
            raise RuntimeError("No CPTAC colon proteomics study found")

        study = prot_studies[0]
        study_id = study['study_id']
        logger.info(f"Found study: {study['study_name']} (ID: {study_id})")
        logger.info(f"  Disease: {study.get('disease_type')}")
        logger.info(f"  Fraction: {study.get('analytical_fraction')}")

        # Step 2b: Get quantitative data
        quant_query = """
        {{
            quantDataMatrix(
                study_id: "{study_id}"
                data_type: "log2_ratio"
                acceptDUA: true
            )
        }}
        """.format(study_id=study_id)

        logger.info("Downloading quantitative proteomics matrix (this may take a minute)...")
        resp = requests.post(pdc_url, json={"query": quant_query}, timeout=300)
        resp.raise_for_status()

        matrix_data = resp.json().get('data', {}).get('quantDataMatrix', [])

        if matrix_data and len(matrix_data) > 1:
            # First row is header (aliquot IDs), rest are gene rows
            header = matrix_data[0]
            rows = matrix_data[1:]

            # Build DataFrame
            import numpy as np
            gene_names = [row[0] for row in rows]
            values = []
            for row in rows:
                vals = []
                for v in row[1:]:
                    try:
                        vals.append(float(v) if v not in (None, '', 'NA') else np.nan)
                    except (ValueError, TypeError):
                        vals.append(np.nan)
                values.append(vals)

            prot_df = pd.DataFrame(
                values,
                index=gene_names,
                columns=header[1:]  # aliquot IDs
            )
            prot_df.index.name = 'Gene'
            prot_df.to_csv(prot_tsv, sep='\t')
            logger.info(f"PDC proteomics saved: {prot_df.shape} → {prot_tsv}")

            # Create minimal clinical TSV with patient IDs
            patient_ids = list(prot_df.columns)
            clin_df = pd.DataFrame({'Patient_ID': patient_ids})
            clin_df['tcga_barcode'] = clin_df['Patient_ID']
            clin_df['aliquot_id'] = clin_df['Patient_ID']
            clin_df.set_index('Patient_ID', inplace=True)
            clin_df.to_csv(clin_tsv, sep='\t')
            logger.info(f"Clinical IDs saved: {clin_df.shape} → {clin_tsv}")

            return prot_tsv, clin_tsv

        else:
            logger.error("PDC returned empty quantitative matrix")
            raise RuntimeError("Empty PDC response")

    except Exception as e:
        logger.error(f"PDC API download also failed: {e}")
        import traceback
        traceback.print_exc()
        logger.info(
            "\n╔══════════════════════════════════════════════════════════╗\n"
            "║  CPTAC proteomics could not be downloaded.              ║\n"
            "║  This is OK for initial runs — only 95/690 patients     ║\n"
            "║  have CPTAC data; the rest are zero-filled anyway.      ║\n"
            "║                                                         ║\n"
            "║  To download manually:                                  ║\n"
            "║  1. Go to: https://pdc.cancer.gov                       ║\n"
            "║  2. Search for 'CPTAC Colon' → Proteome study           ║\n"
            "║  3. Download the quantitative data matrix                ║\n"
            f"║  4. Save as: {prot_tsv:<40}║\n"
            "╚══════════════════════════════════════════════════════════╝"
        )
        return None, None


