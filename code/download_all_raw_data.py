# STEP 1: Download TCGA RNA-seq from GDC
# ═══════════════════════════════════════════════════════════════════════════════

def download_gdc_rnaseq(output_dir: str) -> str:
    """
    Download TCGA-COAD/READ RNA-seq STAR gene counts from GDC.

    Returns path to the manifest file.
    """
    import requests

    gdc_dir = os.path.join(output_dir, 'gdc_star_counts')
    manifest_path = os.path.join(output_dir, 'gdc_manifest.tsv')
    os.makedirs(gdc_dir, exist_ok=True)

    # ─── Step 1a: Query GDC API for file list ─────────────────────────────
    logger.info("=" * 60)
    logger.info("STEP 1: Downloading TCGA-COAD/READ RNA-seq from GDC")
    logger.info("=" * 60)

    filters = {
        "op": "and",
        "content": [
            {
                "op": "in",
                "content": {
                    "field": "cases.project.project_id",
                    "value": ["TCGA-COAD", "TCGA-READ"]
                }
            },
            {
                "op": "=",
                "content": {
                    "field": "data_category",
                    "value": "Transcriptome Profiling"
                }
            },
            {
                "op": "=",
                "content": {
                    "field": "data_type",
                    "value": "Gene Expression Quantification"
                }
            },
            {
                "op": "=",
                "content": {
                    "field": "analysis.workflow_type",
                    "value": "STAR - Counts"
                }
            },
        ]
    }

    params = {
        "filters": json.dumps(filters),
        "fields": (
            "file_id,file_name,file_size,md5sum,state,"
            "cases.submitter_id,cases.project.project_id,"
            "cases.samples.sample_type"
        ),
        "format": "JSON",
        "size": "2000",
    }

    logger.info("Querying GDC API for TCGA-COAD/READ STAR count files...")
    resp = requests.get("https://api.gdc.cancer.gov/files", params=params)
    resp.raise_for_status()
    data = resp.json()
    hits = data['data']['hits']
    logger.info(f"GDC API returned {len(hits)} files")

    # Filter to Primary Tumor samples only
    primary_hits = []
    for h in hits:
        samples = []
        for c in h.get('cases', []):
            for s in c.get('samples', []):
                samples.append(s.get('sample_type', ''))
        if any('Primary Tumor' in s or 'Primary' in s for s in samples):
            primary_hits.append(h)

    if not primary_hits:
        # If sample_type filtering doesn't work, keep all
        primary_hits = hits
        logger.warning("Could not filter by sample type — keeping all files")

    logger.info(f"After Primary Tumor filter: {len(primary_hits)} files")

    # ─── Step 1b: Write GDC manifest ──────────────────────────────────────
    manifest_rows = []
    case_mapping_rows = []
    for h in primary_hits:
        case_id = ''
        project = ''
        for c in h.get('cases', []):
            case_id = c.get('submitter_id', '')
            project = c.get('project', {}).get('project_id', '')
            break

        manifest_rows.append({
            'id': h['file_id'],
            'filename': h['file_name'],
            'md5': h.get('md5sum', ''),
            'size': h.get('file_size', 0),
            'state': h.get('state', 'released'),
        })
        case_mapping_rows.append({
            'id': h['file_id'],
            'filename': h['file_name'],
            'case_id': case_id,
            'project': project,
            'size': h.get('file_size', 0),
        })

    import pandas as pd

    # Save standard GDC manifest (for gdc-client)
    manifest_df = pd.DataFrame(manifest_rows)
    manifest_df.to_csv(manifest_path, sep='\t', index=False)
    logger.info(f"GDC manifest saved: {manifest_path} ({len(manifest_df)} files)")

    # Save case mapping (for our processing pipeline)
    case_df = pd.DataFrame(case_mapping_rows)
    case_path = manifest_path.replace('.tsv', '_with_cases.tsv')
    case_df.to_csv(case_path, sep='\t', index=False)
    logger.info(f"Case mapping saved: {case_path}")

    # ─── Step 1c: Download using gdc-client ───────────────────────────────
    gdc_client = shutil.which('gdc-client')

    if gdc_client:
        logger.info(f"\nDownloading {len(manifest_df)} files with gdc-client...")
        logger.info(f"Output directory: {gdc_dir}")
        logger.info("This may take 30-120 minutes depending on connection speed...\n")

        cmd = [
            gdc_client, 'download',
            '-m', manifest_path,
            '-d', gdc_dir,
            '--n-processes', '4',     # parallel downloads
            '--retry-amount', '5',
        ]
        result = subprocess.run(cmd, capture_output=False)
        if result.returncode != 0:
            logger.error("gdc-client download failed — see output above")
            logger.info("You can retry manually: gdc-client download -m "
                        f"{manifest_path} -d {gdc_dir}")
        else:
            logger.info("GDC download complete!")
    else:
        # Fallback: download via GDC API (slower, one at a time)
        logger.info("\ngdc-client not available — using GDC API direct download")
        logger.info("(This is slower; consider installing gdc-client for parallel downloads)")

        from tqdm import tqdm

        for i, row in tqdm(enumerate(manifest_rows), total=len(manifest_rows),
                           desc="Downloading GDC files"):
            file_id = row['id']
            filename = row['filename']
            out_dir = os.path.join(gdc_dir, file_id)
            out_path = os.path.join(out_dir, filename)

            if os.path.exists(out_path):
                continue  # already downloaded

            os.makedirs(out_dir, exist_ok=True)

            url = f"https://api.gdc.cancer.gov/data/{file_id}"
            try:
                r = requests.get(url, stream=True, timeout=60)
                r.raise_for_status()
                with open(out_path, 'wb') as f:
                    for chunk in r.iter_content(chunk_size=8192):
                        f.write(chunk)
            except Exception as e:
                logger.warning(f"Failed to download {file_id}: {e}")
                continue

        logger.info("GDC API download complete!")

    # Count downloaded files
    n_downloaded = sum(1 for d in os.listdir(gdc_dir)
                       if os.path.isdir(os.path.join(gdc_dir, d)))
    logger.info(f"Downloaded files: {n_downloaded} / {len(manifest_df)}")

    return manifest_path


