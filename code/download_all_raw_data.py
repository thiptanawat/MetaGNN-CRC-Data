# STEP 3: Download Recon3D MATLAB model
# ═══════════════════════════════════════════════════════════════════════════════

def download_recon3d(output_dir: str) -> str:
    """
    Download Recon3D v3.01 MATLAB model from VMH.

    The .mat file is hosted at:
      https://www.vmh.life/files/reconstructions/Recon/3.01/Recon3DModel_301.mat

    Returns path to .mat file.
    """
    import requests
    from tqdm import tqdm

    logger.info("\n" + "=" * 60)
    logger.info("STEP 3: Downloading Recon3D v3.01 MATLAB model")
    logger.info("=" * 60)

    mat_path = os.path.join(output_dir, 'Recon3DModel_301.mat')

    if os.path.exists(mat_path):
        size_mb = os.path.getsize(mat_path) / (1024**2)
        if size_mb > 1.0:  # Actual file is ~1.4 MB (compressed sparse matrices)
            logger.info(f"Recon3D already exists ({size_mb:.1f} MB) — skipping")
            return mat_path

    # Try known VMH download URLs (in order of preference)
    urls = [
        "https://www.vmh.life/files/reconstructions/Recon/3.01/Recon3DModel_301.mat",
        "https://www.vmh.life/files/reconstructions/Recon/3.0/Recon3D.mat",
        "https://www.vmh.life/files/reconstructions/Recon/Recon3DModel_301.mat",
    ]

    for url in urls:
        logger.info(f"Trying: {url}")
        try:
            resp = requests.get(url, stream=True, timeout=30)
            if resp.status_code == 200:
                total = int(resp.headers.get('content-length', 0))
                with open(mat_path, 'wb') as f:
                    with tqdm(total=total, unit='B', unit_scale=True,
                              desc="Recon3D.mat") as pbar:
                        for chunk in resp.iter_content(chunk_size=65536):
                            f.write(chunk)
                            pbar.update(len(chunk))

                size_mb = os.path.getsize(mat_path) / (1024**2)
                logger.info(f"Recon3D downloaded: {size_mb:.0f} MB → {mat_path}")
                return mat_path
            else:
                logger.warning(f"  HTTP {resp.status_code} — trying next URL")
        except Exception as e:
            logger.warning(f"  Failed: {e} — trying next URL")

    # If auto-download fails, provide manual instructions
    logger.error(
        "\nCould not auto-download Recon3D .mat file.\n"
        "Please download manually:\n"
        "  1. Go to: https://www.vmh.life/#downloadview\n"
        "  2. Download 'Recon3DModel_301.mat' (MATLAB format)\n"
        f"  3. Save to: {mat_path}\n"
        "  4. Re-run this script"
    )
    return mat_path


