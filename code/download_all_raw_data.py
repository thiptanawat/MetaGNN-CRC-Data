def check_dependencies():
    """Check that required tools and packages are available."""
    issues = []

    # Check Python packages
    for pkg in ['requests', 'pandas', 'numpy', 'tqdm']:
        try:
            __import__(pkg)
        except ImportError:
            issues.append(f"Missing Python package: {pkg}  → pip install {pkg}")

    # Check gdc-client (optional but needed for Step 1)
    gdc_path = shutil.which('gdc-client')
    if gdc_path is None:
        issues.append(
            "gdc-client not found in PATH.\n"
            "  Install option 1: conda install -c bioconda gdc-client\n"
            "  Install option 2: Download from https://gdc.cancer.gov/access-data/gdc-data-transfer-tool\n"
            "  Install option 3: This script will try to use the GDC API as fallback"
        )
    else:
        logger.info(f"gdc-client found: {gdc_path}")

    if issues:
        logger.warning("Some dependencies missing (may affect certain steps):")
        for issue in issues:
            logger.warning(f"  • {issue}")
    else:
        logger.info("All dependencies satisfied")

    return len(issues) == 0


